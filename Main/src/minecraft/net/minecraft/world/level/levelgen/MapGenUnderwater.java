	public void generate(IChunkProvider chunkProviderGenerate, World world, int xChunk, int zChunk, byte[] data) { 
		this.thisChunk.chestY = -1;
		this.thisChunk.specialY = -1;
		
		// First of all, get average height
		int sumOfHeights = 0;
		for(int z = 0; z < 16; z ++) {
			for(int x = 0; x < 16; x ++) {
				int h = this.heightMap[z << 4 | x];
				sumOfHeights += h;
			}
		}
		this.averageHeight = sumOfHeights >> 8;
		this.baseHeight = this.averageHeight;

		// Then get deviation
		float sumOfDeviations = 0;
		for(int z = 0; z < 16; z ++) {
			for(int x = 0; x < 16; x ++) {
				float deviation = Math.abs(this.heightMap[z << 4 | x] - this.baseHeight);
				sumOfDeviations += deviation * deviation;
			}
		}
		float averageDeviation = (float) Math.sqrt(sumOfDeviations / 256.0F);

		// Not too bumpy
		if(averageDeviation <= 2.0F && rand.nextInt(16) == 1) {
			int feature = rand.nextInt(NUM_FEATURES);
			int featureSize = this.getFeatureSize(feature);
			int featureHeight = this.getFeatureHeight(feature);
			
			// horizontal placement
			int x0 = rand.nextInt(15 - featureSize);
			int z0 = rand.nextInt(15 - featureSize);
			
			// Does it fit?
			int y0 = this.heightMap[(z0 + (featureSize >> 1) << 4 | (x0 + featureSize >> 1))] - 1;
			
			if(y0 + featureHeight < 63) {
				this.raise(x0, y0, z0, featureSize, featureSize, data);
				this.drawFeature(feature, x0, y0, z0, rand.nextBoolean(), this.thisChunk);
				this.thisChunk.hasUnderwaterRuin = true;
			}
		}
	}
	
