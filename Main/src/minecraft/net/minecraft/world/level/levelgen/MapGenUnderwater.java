	public void raise(int x0, int y0, int z0, int w, int h, byte[] data) {
		if(y0 < 16) return;
		
		// Builds a w*h platform made of dirt from x0, y0, z0 to ground level 
		// assuming underwater!
		
		for(int x = x0; x < x0 + w - 1; x ++) {
			for(int z = z0; z < z0 + h - 1; z ++) {
				int y = y0;
				int index = x << 11 | z << 7 | y;
				while(data[index] == (byte)Block.waterStill.blockID && y > 16) {
					data[index] = (byte)Block.dirt.blockID;
					index --;
					y --;
				}
			}
		}
	}
	
	public void populate(World world, Random rand, int chunkX, int chunkZ, Chunk chunk) {
		int x0 = chunkX * 16;
		int z0 = chunkZ * 16;
		
		if(chunk.hasUnderwaterRuin) {
			// Set light
			if(chunk.specialY != -1) {
				world.setBlockAndMetadataWithNotify(x0 + chunk.specialX, chunk.specialY, z0 + chunk.specialZ, Block.shinyGlass.blockID, 0);
			}
			
			// Set chest
			if(chunk.chestY != -1) {
				world.setBlockWithNotify(x0 + chunk.chestX, chunk.chestY, z0 + chunk.chestZ, Block.chest.blockID);
				TileEntityChest tec = (TileEntityChest)world.getBlockTileEntity(x0 + chunk.chestX, chunk.chestY, z0 + chunk.chestZ);
				if(tec != null && tec.getSizeInventory() > 0) {
					int ni = rand.nextInt(4) + rand.nextInt(4) + 2;
		
					for(int i = 0; i < ni; ++i) {
						tec.setInventorySlotContents(i, this.getTreasure(1 + rand.nextInt(3), rand));
					}
				}
			}
		}
	}
	
