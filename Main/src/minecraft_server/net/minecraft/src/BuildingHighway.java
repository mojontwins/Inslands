	public static boolean generate(int chunkX, int chunkZ, Random rand, byte[] data, boolean airborne) {				
		boolean xAligned = (chunkX % 64) == 32;
		boolean zAligned = (chunkZ % 64) == 32;
		
		if(!xAligned && !zAligned) return false;

		// Section depends on coordinates
		if(xAligned && zAligned) {
			genCrossSection(40, data);
		} 
		
		if (xAligned) {
			genStraightZSection(40, data);
		} 
		
		if (zAligned) {
			genStraightXSection(40, data);
		}

		dropBombs(rand, data, chunkX, chunkZ);

		BlockArrayUtils blockArrayUtils = new BlockArrayUtils(data, chunkX, chunkZ);
		if(airborne) {
			int y = 40; int size = 8;
			int iterations = 5 + rand.nextInt(5);
			for(int i = 0; i < iterations; i ++) {
				blockArrayUtils.drawSphere(2 + rand.nextInt(12), y, 2 + rand.nextInt(12), size + rand.nextInt(4), 0);
				if(size > 2) size -= rand.nextInt(3);
				y += 1 + rand.nextInt(3);
			}
		}
		
		return true;
	}
	
	public static void dropBombs(Random rand, byte[] data, int chunkX, int chunkZ) {
		if (rand.nextInt(7) == 0) {
			BlockArrayUtils blockArrayUtils = new BlockArrayUtils(data, chunkX, chunkZ);
			
			// Bomb a bit
			int yBomb = 90;
			int hits = 1 + rand.nextInt(10);
			for (int i = 0; i < hits && yBomb > 70; i ++) {
				int radius = rand.nextInt(8) + 2; 
				if (yBomb - radius < 64) radius = yBomb - 64; 
				blockArrayUtils.drawSphere(2 + rand.nextInt(12), yBomb, 2 + rand.nextInt(12), radius, 0);
				yBomb -= rand.nextInt(8) + 2;
			}
		}
	}

